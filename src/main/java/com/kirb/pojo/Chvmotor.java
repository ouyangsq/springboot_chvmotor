package com.kirb.pojo;

public class Chvmotor {
    public int id;
    public String motername;
    public String modelno;
    public String modedate;
    public String cap;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMotername() {
        return motername;
    }

    public void setMotername(String motername) {
        this.motername = motername;
    }

    public String getModelno() {
        return modelno;
    }

    public void setModelno(String modelno) {
        this.modelno = modelno;
    }

    public String getModedate() {
        return modedate;
    }

    public void setModedate(String modedate) {
        this.modedate = modedate;
    }

    public String getCap() {
        return cap;
    }

    public void setCap(String cap) {
        this.cap = cap;
    }
}
